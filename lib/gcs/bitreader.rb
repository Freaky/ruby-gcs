  def initialize(io)
    @io = io
    reset
  end

  def reset
    @buffer = 0
    @unused = 0
  end

  def read_bits(nbits)
    ret = 0
    rbits = nbits

    while rbits > @unused
      ret |= @buffer << (rbits - @unused)
      rbits -= @unused

      @buffer = @io.readbyte
      @unused = 8
    end

    if rbits > 0
      ret |= @buffer >> (@unused - rbits)
      @buffer &= MASKS[@unused - rbits];
      @unused -= rbits
    end

    ret
  end

  def read_bit
    read_bits(1)
  end

  def seek(bitpos)
    reset
    @io.seek(bitpos / 8)
    read_bits(bitpos % 8)
    true
  end

  alias pos= seek

  def io
    @io
  end
end
